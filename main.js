const navLinks = document.querySelectorAll('.nav-link');  // Все пункты меню
const pages = document.querySelectorAll('.page');         // Все страницы

navLinks.forEach(link => {
    link.addEventListener('click', function(e) {
        e.preventDefault();                               // Отменяем переход по ссылке
        const targetPage = this.getAttribute('data-page'); // Получаем ID целевой страницы

        // Убираем класс active у всех пунктов меню
        navLinks.forEach(l => l.classList.remove('active'));
        // Добавляем класс active текущему пункту
        this.classList.add('active');

        // Скрываем все страницы
        pages.forEach(p => p.classList.remove('active'));
        // Показываем нужную страницу
        document.getElementById('page-' + targetPage).classList.add('active');

        // Прокручиваем страницу вверх
        window.scrollTo(0, 0);

        // Если перешли на страницу курса валют и график ещё не инициализирован
        if (targetPage === 'currency' && !window.chartInitialized) {
            initChart();                                   // Создаём график
            window.chartInitialized = true;                // Отмечаем, что график создан
        }
    });
});

