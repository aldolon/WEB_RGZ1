// ГЕНЕРАЦИЯ ИСТОРИЧЕСКИХ ДАННЫХ ДЛЯ ГРАФИКА
// ============================================
function generateHistoricalData() {
    const data = [];
    const labels = [];
    const today = new Date();
    let rate = cnyToRub;      // Начинаем с текущего курса

    // Генерируем данные за последние 30 дней (с небольшими случайными колебаниями)
    for (let i = 30; i >= 0; i--) {
        const date = new Date(today);
        date.setDate(date.getDate() - i);
        // Случайное изменение курса в пределах ±0.3
        rate = rate + (Math.random() - 0.48) * 0.3;
        // Ограничиваем курс разумными пределами
        rate = Math.max(11.5, Math.min(14.5, rate));
        // Форматируем дату: ДД.ММ
        labels.push(date.toLocaleDateString('ru-RU', { day: '2-digit', month: '2-digit' }));
        data.push(parseFloat(rate.toFixed(4)));
    }

    return { labels, data };
}

