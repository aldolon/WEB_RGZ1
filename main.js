// КОНВЕРТЕР ВАЛЮТ (CNY/RUB)
// ============================================
let cnyToRub = 12.85;        // Курс по умолчанию (если API недоступен)
let chartInstance = null;     // Переменная для хранения экземпляра графика

// Функция получения актуального курса с API ЦБ РФ
async function fetchRates() {
    try {
        const response = await fetch('https://www.cbr-xml-daily.ru/daily_json.js');
        const data = await response.json();
        if (data.Valute && data.Valute.CNY) {
            cnyToRub = data.Valute.CNY.Value;      // Обновляем курс
            document.getElementById('currentRate').textContent = '1 CNY = ' + cnyToRub.toFixed(4) + ' RUB';
        }
    } catch (e) {
        // Если API недоступен, используем значение по умолчанию
        document.getElementById('currentRate').textContent = '1 CNY ≈ ' + cnyToRub.toFixed(4) + ' RUB (оценка)';
    }
    convertCurrency();        // Пересчитываем конвертацию с новым курсом
}

// Функция конвертации валют
function convertCurrency() {
    const amount = parseFloat(document.getElementById('amountFrom').value) || 0;
    const fromCur = document.getElementById('fromCurrency').value;
    const toCur = document.getElementById('toCurrency').value;
    let result;

    if (fromCur === 'CNY' && toCur === 'RUB') {
        result = amount * cnyToRub;      // Юань → Рубль
    } else if (fromCur === 'RUB' && toCur === 'CNY') {
        result = amount / cnyToRub;      // Рубль → Юань
    } else {
        result = amount;                  // Если валюты одинаковые
    }

    document.getElementById('amountTo').value = result.toFixed(2);
}

// Отслеживаем изменения в полях ввода и выбора валют
document.getElementById('amountFrom').addEventListener('input', convertCurrency);
document.getElementById('fromCurrency').addEventListener('change', function() {
    // Автоматически меняем вторую валюту на противоположную
    document.getElementById('toCurrency').value = this.value === 'CNY' ? 'RUB' : 'CNY';
    convertCurrency();
});
document.getElementById('toCurrency').addEventListener('change', function() {
    // Автоматически меняем первую валюту на противоположную
    document.getElementById('fromCurrency').value = this.value === 'CNY' ? 'RUB' : 'CNY';
    convertCurrency();
});

// Кнопка для смены направления конвертации
document.getElementById('swapBtn').addEventListener('click', function() {
    const fromCur = document.getElementById('fromCurrency');
    const toCur = document.getElementById('toCurrency');
    const temp = fromCur.value;
    fromCur.value = toCur.value;
    toCur.value = temp;
    convertCurrency();
});

